def fibonacci(n):
    """Определяет последовательность из n чисел Фибоначчи"""
    # Проверка, что n - положительное целое число
    if not isinstance(n, int) or n <= 0:
        raise ValueError("n должно быть положительным целым числом.")

    # Если требуется только одно число, возвращаем [0]
    if n == 1:
        return [0]

    # Начальная последовательность Фибоначчи для n >= 2
    fib_sequence = [0, 1]

    # Генерация чисел Фибоначчи с помощью цикла
    for i in range(2, n):
        # Добавляем следующее число как сумму двух предыдущих
        fib_sequence.append(fib_sequence[i - 1] + fib_sequence[i - 2])

    # Возвращаем последовательность первых n чисел Фибоначчи
    return fib_sequence[:n]


